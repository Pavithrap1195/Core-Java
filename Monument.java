class Monument{
	
	String located;
	int heightInMeter;
	int areaInHectares;
	
	public Monument(){
		System.out.println("Monument object is created");
	}
	
	public void commemorate(){
		System.out.println("A monument is a type of structure that was explicitly created to commemorate a person or event");
	}
	
	public void setLocated(String located){
		this.located=located;
	}
	
	public String getLocated(){
		return located;
	}
	
	public void setHeightInMeter(int heightInMeter){
		this.heightInMeter=heightInMeter;
	}
	
	public int getHeightInMeter(){
		return heightInMeter;
	}
	
	public void setAreaInHectares(int areaInHectares){
		this.areaInHectares=areaInHectares;
	}
	
	public int getAreaInHectares(){
		return areaInHectares;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}