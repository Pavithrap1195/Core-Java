public class HardDiskDTO{
	
	private String name;
	private double price;
	private boolean isItPortable;
	private int storageInTb;
	
	public HardDiskDTO(){
	}
	
	public void setName(String name){
		this.name=name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setPrice(double price){
		this.price=price;
	}
	
	public double getPrice(){
		return price;
	}
	
	public void setIsItPortable(boolean isItPortable){
		this.isItPortable=isItPortable;
	}
	
	public boolean getIsItPortable(){
		return isItPortable;
	}
	
	public void setStorageInTb(int storageInTb){
		this.storageInTb=storageInTb;
	}
	
	public int getStorageInTb(){
		return storageInTb;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}