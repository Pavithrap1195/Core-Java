public class FruitDTO{
	
	private String name;
	private double priceInKg;
	private String color;
	
	public FruitDTO(){
	}
	
	public void setName(String name){
		this.name=name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setPriceInKg(double priceInKg){
		this.priceInKg=priceInKg;
	}
	
	public double getPriceInKg(){
		return priceInKg;
	}
	
	public void setColor(String color){
		this.color=color;
	}
	
	public String getColor(){
		return color;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}