class Bird{
	
	int id;
	String color;
	String family;
	
	public Bird(){
		System.out.println("Bird object is created");
	}
	
	public void warmBlooded(){
		System.out.println("Birds are a group of warm-blooded vertebrates");
	}
	
	public void setId(int id){
		this.id=id;
	}
	
	public int getId(){
		return id;
	}
	
	public void setColor(String color){
		this.color=color;
	}
	
	public String getColor(){
		return color;
	}
	
	public void setFamily(String family){
		this.family=family;
	}
	
	public String getFamily(){
		return family;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}